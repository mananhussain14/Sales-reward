-- SECTION 7 — Parent indexes (6 explicit; 7 total including the PK)
-- ============================================================================
-- THE duplicate guard: one live invitation per (retailer, canonical email).
-- Partial so any number of settled rows for that pair coexist. This is scoped to
-- the retailer, so the SAME email may hold a PENDING invitation in a DIFFERENT
-- retailer simultaneously — which is exactly why there is deliberately NO globally
-- unique auth-user pending index here (that owner-only rule lives on the owner
-- table and must not constrain staff who may serve multiple retailers).
create unique index retailer_staff_invitations_pending_unique_idx
  on public.retailer_staff_invitations (retailer_organization_id, email)
  where status = 'PENDING';

-- One live token per hash.
create unique index retailer_staff_invitations_token_hash_unique_idx
  on public.retailer_staff_invitations (token_hash)
  where token_hash is not null;

-- The roster question: "which staff invitations does this retailer have, in what
-- state?", newest first.
create index retailer_staff_invitations_retailer_status_created_idx
  on public.retailer_staff_invitations (retailer_organization_id, status, created_at desc);

-- The expiry sweep (used by a future RPC). Partial to the rows a sweep can act on.
create index retailer_staff_invitations_pending_expiry_idx
  on public.retailer_staff_invitations (expires_at)
  where status = 'PENDING';

-- Cross-retailer email lookup.
create index retailer_staff_invitations_email_idx
  on public.retailer_staff_invitations (email);

-- Acceptance-history lookup by Auth user (non-unique; a person may have accepted
-- invitations in several retailers).
create index retailer_staff_invitations_auth_user_idx
  on public.retailer_staff_invitations (auth_user_id)
  where auth_user_id is not null;

