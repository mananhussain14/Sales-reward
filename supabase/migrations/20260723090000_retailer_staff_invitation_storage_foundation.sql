-- Migration: retailer_staff_invitation_storage_foundation
-- Purpose: The storage + authorization-root foundation for Retailer staff
--          invitations. It adds, and only adds:
--            1. public.retailer_staff_invitations — the pending/settled invitation
--               record for a RETAILER_MANAGER or SALES_STAFF invitee, driven by a
--               single application-owned token flow that serves both new and
--               existing Auth accounts.
--            2. public.retailer_invitation_shop_assignments — the immutable set of
--               intended shops preserved while a SALES_STAFF invitation is pending,
--               applied atomically on acceptance by a future RPC.
--            3. public.resolve_retailer_member_organization(text) — the internal,
--               read-only authorization resolver every future staff RPC will call.
--          Plus the validation/immutability triggers, indexes, and default-deny
--          RLS + privilege hardening for the two tables.
--
-- WHY A SEPARATE TABLE FROM public.retailer_invitations
--   The Retailer Owner invitation domain (migrations 15/19/20) is owner-specific
--   and single-owner-specific: get_vendor_retailer_owner_status reads the newest
--   PENDING row for a retailer WITHOUT a role filter, and a GLOBAL unique index
--   (retailer_invitations_auth_user_pending_idx) permits only one pending
--   invitation per Auth user. Staff need the opposite: many staff per retailer, and
--   the same person invited to different retailers. Keeping staff invitations in
--   their own table leaves every owner object BYTE-IDENTICAL, and lets the staff
--   indexes express multi-staff/multi-retailer natively. This migration therefore
--   does NOT touch retailer_invitations, any owner/portal function,
--   retailer_shop_members, roles/permissions, or any existing policy.
--
-- ONE APPLICATION-OWNED TOKEN FLOW (both new and existing accounts)
--   Only SHA-256(raw token) is ever stored (token_hash, 64 lowercase hex), only on
--   a live PENDING invitation, and retained across a delivery failure. The raw
--   token is generated by the server, may appear in the recipient's URL, and is
--   never stored, logged, or audited. There is deliberately NO invitation_kind: the
--   acceptance RPC (a later migration) determines new-vs-existing from the real
--   auth/profile state, never a stored flag. There is NO vendor_organization_id:
--   staff invitations are retailer-internal, issued by the Retailer Owner.
--
-- WHAT THIS MIGRATION DELIBERATELY DOES NOT DO
--   No reserve/resend/revoke/delivery/recipient/acceptance RPC; no
--   expire_stale_retailer_staff_invitations(); no audit-log write; no feature flag;
--   no route/Server Action/email template. Storage + authorization root only.
--   Access to both tables exists solely through future SECURITY DEFINER RPCs.
--
-- Idempotency posture: plain CREATE (no IF NOT EXISTS, no CREATE OR REPLACE). A
--   conflicting existing object FAILS the migration rather than being silently
--   accepted, matching the repository's schema-migration convention. No fixed
--   UUIDs anywhere.
--
-- Dependencies: 20260716124419 (organizations, profiles, organization_members,
--   set_updated_at), 20260716125559 (roles, permissions, role_permissions,
--   member_roles), 20260717094520 (retailer_shops, assert_organization_type),
--   20260722210000 (RETAILER_MANAGER, SALES_STAFF, RETAILER_STAFF_* permissions and
--   their mappings — the resolver and role trigger rely on these).

-- ============================================================================
-- SECTION 1 — public.retailer_staff_invitations (table + FKs + 18 checks)
-- ============================================================================
create table public.retailer_staff_invitations (
  id                       uuid        primary key default gen_random_uuid(),

  -- The retailer the staff member joins. RESTRICT: retailers retire by status,
  -- never hard-deleted.
  retailer_organization_id uuid        not null,

  -- Stored ALREADY canonical (trimmed + lower-cased); enforced by the check below.
  email                    text        not null,

  -- The invitee's real name, collected from the inviting owner. Never invented.
  first_name               text        not null,
  last_name                text        not null,

  -- The target role. A trigger restricts this to an ACTIVE RETAILER_MANAGER or
  -- SALES_STAFF role; RESTRICT follows member_roles.role_id.
  role_id                  uuid        not null,

  status                   text        not null default 'PENDING',

  -- Set only at acceptance (token flow → NULL while PENDING). SET NULL so removing
  -- the Auth user preserves accepted history (see the context checks below).
  auth_user_id             uuid        null,

  -- Set at acceptance to the produced membership. SET NULL for the same reason.
  organization_member_id   uuid        null,

  -- Who issued it. SET NULL follows member_roles.assigned_by.
  invited_by_profile_id    uuid        null,

  -- SHA-256(raw token) as 64 lowercase hex; only on a live PENDING invitation;
  -- retained across a delivery failure; cleared on accept/revoke/expire.
  token_hash               text        null,

  -- The only staff failure code this milestone recognises.
  failure_code             text        null,
  failure_recorded_at      timestamptz null,

  expires_at               timestamptz not null default (now() + interval '24 hours'),
  sent_at                  timestamptz null,
  accepted_at              timestamptz null,
  revoked_at               timestamptz null,

  created_at               timestamptz not null default now(),
  updated_at               timestamptz not null default now(),

  -- Foreign keys (explicit, stable names) --------------------------------------
  constraint retailer_staff_invitations_retailer_fk
    foreign key (retailer_organization_id)
    references public.organizations (id) on delete restrict,
  constraint retailer_staff_invitations_role_fk
    foreign key (role_id)
    references public.roles (id) on delete restrict,
  constraint retailer_staff_invitations_auth_user_fk
    foreign key (auth_user_id)
    references auth.users (id) on delete set null,
  constraint retailer_staff_invitations_member_fk
    foreign key (organization_member_id)
    references public.organization_members (id) on delete set null,
  constraint retailer_staff_invitations_invited_by_fk
    foreign key (invited_by_profile_id)
    references public.profiles (id) on delete set null,

  -- 18 check constraints --------------------------------------------------------
  -- 1. Canonical email, enforced by the database rather than trusted of callers.
  constraint retailer_staff_invitations_email_canonical
    check (
      email = lower(btrim(email))
      and length(email) > 0
      and length(email) <= 254
    ),
  -- 2. Pragmatic shape check. COLLATE "C" is load-bearing (locale-independent
  --    bracket ranges), matching retailer_invitations_email_shape.
  constraint retailer_staff_invitations_email_shape
    check (email collate "C" ~ '^[^[:space:]@]+@[^[:space:]@]+\.[^[:space:]@]+$'),
  -- 3/4. Names non-empty after trimming.
  constraint retailer_staff_invitations_first_name_not_empty
    check (length(btrim(first_name)) > 0),
  constraint retailer_staff_invitations_last_name_not_empty
    check (length(btrim(last_name)) > 0),
  -- 5. Names stored already trimmed.
  constraint retailer_staff_invitations_names_trimmed
    check (first_name = btrim(first_name) and last_name = btrim(last_name)),
  -- 6. Stored lifecycle statuses.
  constraint retailer_staff_invitations_status_allowed
    check (status in ('PENDING', 'ACCEPTED', 'EXPIRED', 'REVOKED')),
  -- 7/8. Status and its timestamp move together (strict equivalence — these
  --      timestamps are not FK columns and never get SET NULL).
  constraint retailer_staff_invitations_accepted_consistent
    check ((status = 'ACCEPTED') = (accepted_at is not null)),
  constraint retailer_staff_invitations_revoked_consistent
    check ((status = 'REVOKED') = (revoked_at is not null)),
  -- 9/10. CONTEXT constraints (one-directional): an acceptance reference may exist
  --       ONLY on an ACCEPTED row, so PENDING/REVOKED/EXPIRED can never carry one.
  --       Written as `ref IS NULL OR status = 'ACCEPTED'` so a later ON DELETE SET
  --       NULL on an accepted history row (which clears the ref while status stays
  --       ACCEPTED) still satisfies the rule and the FK action succeeds.
  --       Transition-time completeness ("both refs required when becoming
  --       ACCEPTED") is enforced by the acceptance-references trigger, not here.
  constraint retailer_staff_invitations_auth_user_context
    check (auth_user_id is null or status = 'ACCEPTED'),
  constraint retailer_staff_invitations_member_context
    check (organization_member_id is null or status = 'ACCEPTED'),
  -- 11. Token hash shape.
  constraint retailer_staff_invitations_token_hash_format
    check (token_hash is null or token_hash collate "C" ~ '^[0-9a-f]{64}$'),
  -- 12. A token may exist only on a live PENDING invitation.
  constraint retailer_staff_invitations_token_hash_context
    check (token_hash is null or status = 'PENDING'),
  -- 13. The only recognised staff failure code.
  constraint retailer_staff_invitations_failure_code_allowed
    check (failure_code is null or failure_code = 'EMAIL_DISPATCH_FAILED'),
  -- 14. Code and its timestamp move together.
  constraint retailer_staff_invitations_failure_consistent
    check ((failure_code is null) = (failure_recorded_at is null)),
  -- 15. A failure may exist only while PENDING (terminal states carry none).
  constraint retailer_staff_invitations_failure_context
    check (failure_code is null or status = 'PENDING'),
  -- 16. Never simultaneously "sent" and "failed".
  constraint retailer_staff_invitations_sent_or_failure
    check (not (sent_at is not null and failure_code is not null)),
  -- 17. A PENDING invitation that was sent must carry its token (scoped to PENDING
  --     so an ACCEPTED row whose token is cleared but whose sent_at persists as
  --     history is unaffected).
  constraint retailer_staff_invitations_pending_sent_has_token
    check (status <> 'PENDING' or sent_at is null or token_hash is not null),
  -- 18. A failure can only exist while the token is present (DELIVERY_FAILED
  --     retains its token for in-app recovery / resend rotation).
  constraint retailer_staff_invitations_failure_requires_token
    check (failure_code is null or token_hash is not null)
);

-- ============================================================================
-- SECTION 2 — Target-role validation (INSERT only; role_id is immutable)
-- ============================================================================
-- Guarantees role_id resolves to an ACTIVE role whose code is exactly
-- RETAILER_MANAGER or SALES_STAFF. A CHECK constraint cannot join roles, so this is
-- a trigger. SECURITY DEFINER + search_path='' so it reads public.roles as its
-- owner regardless of the writer's RLS. No dynamic SQL.
create function public.retailer_staff_invitations_assert_target_role()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_code   text;
  v_status text;
begin
  select r.code, r.status
    into v_code, v_status
  from public.roles r
  where r.id = new.role_id;

  -- BEFORE-row runs before the FK is checked, so a non-existent role can reach here.
  if v_code is null then
    raise exception 'Referenced role does not exist'
      using errcode = 'foreign_key_violation';
  end if;

  -- Rejects RETAILER_OWNER and every Vendor role (absent from the allow-set), and
  -- any inactive role. The message names the rule, never the role.
  if v_status <> 'ACTIVE' or v_code not in ('RETAILER_MANAGER', 'SALES_STAFF') then
    raise exception 'Staff invitation role must be an active Retailer Manager or Sales Staff role'
      using errcode = 'check_violation';
  end if;

  return new;
end;
$$;

revoke all     on function public.retailer_staff_invitations_assert_target_role() from public;
revoke execute on function public.retailer_staff_invitations_assert_target_role() from anon;
revoke execute on function public.retailer_staff_invitations_assert_target_role() from authenticated;

create trigger retailer_staff_invitations_assert_target_role_on_insert
  before insert on public.retailer_staff_invitations
  for each row execute function public.retailer_staff_invitations_assert_target_role();

