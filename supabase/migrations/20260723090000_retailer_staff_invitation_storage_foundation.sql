-- ============================================================================
-- SECTION 9 — public.retailer_invitation_shop_assignments (table + FKs + unique)
-- ============================================================================
-- The immutable set of intended shops for a SALES_STAFF invitation, applied to
-- retailer_shop_members atomically on acceptance by a future RPC. No removed_at (an
-- immutable intended set), no updated_at (never updated), no assigned_by (the
-- parent's invited_by_profile_id records the actor), no status.
create table public.retailer_invitation_shop_assignments (
  id                            uuid        primary key default gen_random_uuid(),

  -- CASCADE: an intended-shop row has no meaning without its invitation, mirroring
  -- member_roles.organization_member_id and retailer_shop_members.
  retailer_staff_invitation_id  uuid        not null,

  -- RESTRICT: shops retire by status, never hard-deleted; a shop referenced by an
  -- invitation cannot be erased.
  retailer_shop_id              uuid        not null,

  created_at                    timestamptz not null default now(),

  constraint retailer_invitation_shop_assignments_invitation_fk
    foreign key (retailer_staff_invitation_id)
    references public.retailer_staff_invitations (id) on delete cascade,
  constraint retailer_invitation_shop_assignments_shop_fk
    foreign key (retailer_shop_id)
    references public.retailer_shops (id) on delete restrict,

  constraint retailer_invitation_shop_assignments_unique
    unique (retailer_staff_invitation_id, retailer_shop_id)
);

-- ============================================================================
-- SECTION 10 — Shop-assignment INSERT validation
-- ============================================================================
-- Defense in depth over the future reserve RPC: the shop must belong to the
-- invitation's own retailer, and only SALES_STAFF invitations may carry shop rows
-- (RETAILER_MANAGER explicitly rejected). Does NOT enforce the "at least one shop"
-- rule (a multi-row invariant that belongs to the reserve RPC).
create function public.retailer_invitation_shop_assignments_assert_valid()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_inv_retailer  uuid;
  v_role_id       uuid;
  v_role_code     text;
  v_shop_retailer uuid;
begin
  select i.retailer_organization_id, i.role_id
    into v_inv_retailer, v_role_id
  from public.retailer_staff_invitations i
  where i.id = new.retailer_staff_invitation_id;

  if v_inv_retailer is null then
    raise exception 'Referenced staff invitation does not exist'
      using errcode = 'foreign_key_violation';
  end if;

  select s.retailer_organization_id
    into v_shop_retailer
  from public.retailer_shops s
  where s.id = new.retailer_shop_id;

  if v_shop_retailer is null then
    raise exception 'Referenced shop does not exist'
      using errcode = 'foreign_key_violation';
  end if;

  if v_inv_retailer <> v_shop_retailer then
    raise exception 'Invitation shop must belong to the same Retailer as the invitation'
      using errcode = 'check_violation';
  end if;

  select r.code
    into v_role_code
  from public.roles r
  where r.id = v_role_id;

  if v_role_code = 'RETAILER_MANAGER' then
    raise exception 'Retailer Manager invitations cannot have shop assignments'
      using errcode = 'check_violation';
  end if;

  -- Only SALES_STAFF may carry shop rows. Belt and braces: the parent role trigger
  -- already restricts an invitation's role to {RETAILER_MANAGER, SALES_STAFF}, so
  -- this rejects anything that is not SALES_STAFF.
  if v_role_code is distinct from 'SALES_STAFF' then
    raise exception 'Invitation shop assignments are permitted only for Sales Staff invitations'
      using errcode = 'check_violation';
  end if;

  return new;
end;
$$;

revoke all     on function public.retailer_invitation_shop_assignments_assert_valid() from public;
revoke execute on function public.retailer_invitation_shop_assignments_assert_valid() from anon;
revoke execute on function public.retailer_invitation_shop_assignments_assert_valid() from authenticated;

create trigger retailer_invitation_shop_assignments_assert_valid_on_insert
  before insert on public.retailer_invitation_shop_assignments
  for each row execute function public.retailer_invitation_shop_assignments_assert_valid();

