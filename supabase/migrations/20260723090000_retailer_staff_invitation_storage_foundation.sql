-- ============================================================================
-- SECTION 4 — Immutable invitation identity
-- ============================================================================
-- Once inserted, the retailer, recipient email, and target role are fixed. Changing
-- any of them is a different invitation and must go through revoke + replacement.
-- Names, lifecycle timestamps, token_hash, failure fields, status, auth_user_id and
-- organization_member_id remain updatable by future controlled RPCs.
create function public.retailer_staff_invitations_assert_immutable_identity()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if new.retailer_organization_id is distinct from old.retailer_organization_id then
    raise exception 'Staff invitation Retailer is immutable; revoke and re-issue to change it'
      using errcode = 'check_violation';
  end if;
  if new.email is distinct from old.email then
    raise exception 'Staff invitation recipient email is immutable; revoke and re-issue to change it'
      using errcode = 'check_violation';
  end if;
  if new.role_id is distinct from old.role_id then
    raise exception 'Staff invitation role is immutable; revoke and re-issue to change it'
      using errcode = 'check_violation';
  end if;
  return new;
end;
$$;

revoke all     on function public.retailer_staff_invitations_assert_immutable_identity() from public;
revoke execute on function public.retailer_staff_invitations_assert_immutable_identity() from anon;
revoke execute on function public.retailer_staff_invitations_assert_immutable_identity() from authenticated;

-- UPDATE OF narrows firing to statements touching one of the three columns; the
-- WHEN clause narrows further to statements that actually change one. Fires
-- alphabetically before the other BEFORE-UPDATE triggers, so a forbidden change is
-- rejected first.
create trigger retailer_staff_invitations_assert_immutable_identity_on_update
  before update of retailer_organization_id, email, role_id
  on public.retailer_staff_invitations
  for each row
  when (
    new.retailer_organization_id is distinct from old.retailer_organization_id
    or new.email is distinct from old.email
    or new.role_id is distinct from old.role_id
  )
  execute function public.retailer_staff_invitations_assert_immutable_identity();

-- ============================================================================
-- SECTION 5 — Acceptance-reference validation
-- ============================================================================
-- Transition-time completeness: a row that BECOMES ACCEPTED must record both its
-- Auth user and its membership. Deliberately does NOT fire when an already-ACCEPTED
-- historical row later has either reference cleared by ON DELETE SET NULL (status
-- stays ACCEPTED, so neither WHEN clause matches) — that is how accepted history
-- survives Auth-user or membership deletion. The context checks (9/10) allow the
-- cleared state; this trigger only guards the transition INTO ACCEPTED.
create function public.retailer_staff_invitations_assert_acceptance_references()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if new.auth_user_id is null or new.organization_member_id is null then
    raise exception 'An accepted staff invitation must record both its Auth user and its membership'
      using errcode = 'check_violation';
  end if;
  return new;
end;
$$;

revoke all     on function public.retailer_staff_invitations_assert_acceptance_references() from public;
revoke execute on function public.retailer_staff_invitations_assert_acceptance_references() from anon;
revoke execute on function public.retailer_staff_invitations_assert_acceptance_references() from authenticated;

create trigger retailer_staff_invitations_assert_acceptance_refs_on_insert
  before insert on public.retailer_staff_invitations
  for each row
  when (new.status = 'ACCEPTED')
  execute function public.retailer_staff_invitations_assert_acceptance_references();

create trigger retailer_staff_invitations_assert_acceptance_refs_on_update
  before update of status
  on public.retailer_staff_invitations
  for each row
  when (new.status = 'ACCEPTED' and old.status is distinct from 'ACCEPTED')
  execute function public.retailer_staff_invitations_assert_acceptance_references();

