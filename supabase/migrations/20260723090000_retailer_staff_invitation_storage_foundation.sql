-- ============================================================================
-- SECTION 14 — Internal authorization resolver
-- ============================================================================
-- resolve_retailer_member_organization(target_permission_code text) -> uuid
--
-- Resolves the ONE Retailer organization in which the caller holds
-- target_permission_code through an ACTIVE membership + ACTIVE role, or NULL. This
-- is the authorization ROOT for every future staff RPC: staff actions authorize
-- from the authenticated Retailer member, NOT through the Vendor relationship.
--
-- It mirrors public.resolve_retailer_owner_organization() condition-for-condition
-- with ONE deliberate difference: it does NOT hard-code r.code = 'RETAILER_OWNER'.
-- Instead it admits any ACTIVE role that holds the requested permission, so under
-- the current mappings (migration 20260722210000):
--   * RETAILER_STAFF_MANAGE / _SHOP_ASSIGN are mapped only to RETAILER_OWNER ->
--     resolver(MANAGE|SHOP_ASSIGN) admits ONLY owners.
--   * RETAILER_STAFF_READ is mapped to RETAILER_OWNER and RETAILER_MANAGER ->
--     resolver(READ) admits both; a manager calling a MANAGE-gated RPC resolves
--     NULL and cannot invite.
--
-- DISTINCT organization ids: multiple qualifying roles in ONE retailer collapse to
-- one organization, so "owner who also holds another qualifying role" still
-- resolves that single retailer. The count guard returns the id only when exactly
-- one distinct retailer qualifies, and NULL for zero or two-or-more (fail closed;
-- there is no owner-facing organization switcher yet). No browser-supplied
-- organization id — the caller is auth.uid() and nothing else.
create function public.resolve_retailer_member_organization(
  target_permission_code text
)
returns uuid
language sql
stable
security definer
set search_path = ''
as $$
  with qualifying as (
    select distinct o.id
    from public.profiles p
    join public.organization_members m on m.user_id = p.id
    join public.organizations o on o.id = m.organization_id
    join public.member_roles mr on mr.organization_member_id = m.id
    join public.roles r on r.id = mr.role_id
    join public.role_permissions rp on rp.role_id = r.id
    join public.permissions perm on perm.id = rp.permission_id
    where target_permission_code is not null
      and auth.uid() is not null
      and p.id = auth.uid()
      and p.status = 'ACTIVE'
      and m.status = 'ACTIVE'
      and o.status = 'ACTIVE'
      and o.organization_type = 'RETAILER'
      and r.status = 'ACTIVE'
      and perm.code = target_permission_code
  )
  select q.id
  from qualifying q
  where (select count(*) from qualifying) = 1;
$$;

-- Internal only: reachable solely from future SECURITY DEFINER RPCs that run as
-- this function's owner. Not granted to any browser role.
revoke all     on function public.resolve_retailer_member_organization(text) from public;
revoke execute on function public.resolve_retailer_member_organization(text) from anon;
revoke execute on function public.resolve_retailer_member_organization(text) from authenticated;

